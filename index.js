function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, z) {
  return a * z
}

function divide(a, z) {
  return a / z
}

function inc(num) {
  return num += 1
}

function dec(num) {
  return num -= 1
}

function makeInt(str) {
  return parseInt(str, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}